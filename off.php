		<h3>Konsultacja psychologiczna</h3>
			<br>
	<div>
<div  id="col2">	
			To rozmowa z psychologiem na temat doświadczanych trudności.
			<br>Celem konsultacji jest omówienie problemu i próba opracowania strategii poradzenia sobie z&nbspnapotkanymi trudnościami.
			<br>Czasem jest to pojedyncze spotkanie, czasem kilka, często staje się wstępem do dłuższej pracy w ramach terapii indywidualnej.
	</div>	
	<br><br>
	<div id="col1">
		<h3>Konsultacja wychowawcza</h3>
			<br>
	<div>
<div  id="col2">	
			Forma pomocy psychologicznej skierowana do rodziców, którzy doświadczają trudności wychowawczych w relacji ze swoim dzieckiem.
			<br>Na pierwsze spotkanie przychodzi sam rodzic/rodzice, czasem jest to wystarczające, aby&nbspokreślić sposób rozwiązania problemu.
			<br>Druga konsultacja poświęcona jest rozmowie z dzieckiem/nastolatkiem.
	</div>	
	<br><br>
	<div id="col1">
		<h3>Psychoterapia indywidualna</h3>
			<br>
	<div>
<div  id="col2">	
			Regularne spotkania skoncentrowane na ustalonym wspólnie z klientem obszarze problemowym. Efektem terapii jest wprowadzenie zmiany oraz poprawa jakości dotychczasowego życia.
			<br>Spotkania odbywają się zwykle raz w tygodniu, ich ilość oraz cel określane są wspólnie przez terapeutę i klienta na początku współpracy.
			<br>Psychoterapia jest szczególnie skuteczna w przypadku:
				<ul>
					<li>odczuwania lęku, niepokoju (nieustający niepokój, zamartwianie się, ataki paniki, obsesyjne myśli, fobie),
					<li>traumatycznych przeżyć (wypadek, przemoc),
					<li>przeżywania żałoby lub straty,
					<li>przeżywania silnego przygnębienia i smutku (depresja, apatia, myśli samobójcze, wahania nastroju),
					<li>niskiego poczucia własnej wartości,
					<li>braku satysfakcjonujących relacji z innymi ludźmi (nieśmiałość, poczucie niezrozumienia, kłopoty w związku partnerskim w rodzinie),
					<li>zaburzeń odżywiania (anoreksja, bulimia),
					<li>chęci poznania i zrozumienia samego siebie.
				</ul>
	</div>			
	
	
