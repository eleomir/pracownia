<h3>Kursy Szkoły Pamięci Wojakowskich</h3>
Zapraszamy do udziału w kursach treningowych. Ich celem jest zapoznanie się z&nbsppodstawowymi technikami pamięciowymi, poprawa koncentracji, nabycie i doskonalenie umiejętności uczenia się. 
<br><br>
Szkoła Pamięci SPW to ogólnopolska firma z ponad 20letnim doświadczeniem w&nbspprowadzeniu szkoleń z zakresu rozwoju osobistego. Głównym celem zajęć jest rozwój pamięci, koncentracji, uwagi oraz poprawy umiejętności uczenia się i przyswajania informacji. 
<br><br>
Proponowane treningi złożone są z zestawu ćwiczeń, które pobudzają umysł do&nbspintensywnej pracy. Zadania dobrane są do odpowiednio do grupy wiekowej, zaplanowanej intensywności treningu oraz założonych celów. 
<br><br>
Zajęcia prowadzone są w niewielkich grupach, jedno spotkanie trwa dwie godziny lekcyjne. 
